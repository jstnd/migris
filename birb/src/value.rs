#[derive(Debug)]
pub enum Value {
    Null,
    Bytes(Vec<u8>),
    Date(chrono::DateTime<chrono::Utc>),
    Decimal(rust_decimal::Decimal),
    String(String),
    Time(chrono::NaiveTime),
    F32(f32),
    F64(f64),
    I8(i8),
    I16(i16),
    I32(i32),
    I64(i64),
    U8(u8),
    U16(u16),
    U32(u32),
    U64(u64),
}
